sub finddefine()
{
  my($classname, $definename) = (@_);
  $cmd = 'egrep ' . $classname .  ' ' . $dir . '*.h ' . $dir . '*.cpp | egrep "public" | egrep -v "Support[.]"' . '&> /dev/null';
  if( system($cmd)) {
    print   "\/\/ ";
  }
  print   "#define " . $definename . "\n";
}



