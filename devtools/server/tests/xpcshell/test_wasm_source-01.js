var gThreadFront;

add_task(
  threadFrontTest(
    async ({ threadFront, debuggee, client }) => {
      gThreadFront = threadFront;
      gDebuggee = debuggee;

      await gThreadFront.reconfigure({
        observeAsmJS: true,
      });

      test_source();
    },
    { waitForFinish: true, doNotRunWorker: true }
  )
);

