    twpLang.languages["zh"] = twpLang.languages["zh-CN"]

    twpLang.codeToLanguage = function (langCode) {
        let uiLanguage = chrome.i18n.getUILanguage()
        uiLanguage = twpLang.fixLanguageCode(uiLanguage)
        langCode = twpLang.fixLanguageCode(langCode)

        if (!twpLang.languages[uiLanguage]) {
            uiLanguage = "en"
        }

        return twpLang.languages[uiLanguage][langCode]
    }

    twpLang.fixLanguageCode = function (langCode) {
        if (typeof langCode !== "string") return;

        const langCodeSplitted = langCode.trim().split("-")
        if (langCodeSplitted.length === 0) return;
        langCodeSplitted[0] = langCodeSplitted[0].toLowerCase()

        let fixedLangCode = null

        if (langCodeSplitted[0] === "zh" && typeof langCodeSplitted[1] !== "undefined") {
            langCodeSplitted[1] = langCodeSplitted[1].toUpperCase()
            if (langCodeSplitted[1] === "CN" || langCodeSplitted[1] === "TW") {
                fixedLangCode = langCode.trim()
            }
        }

        if (!fixedLangCode) {
            fixedLangCode = langCodeSplitted[0]
        }

        return fixedLangCode
    }

    twpLang.checkLanguageCode = function (langCode) {
        langCode = twpLang.fixLanguageCode(langCode)
        if (typeof langCode !== "string") return;

        for (const lang in twpLang.languages["en"]) {
            if (lang === langCode) {
                return langCode
            }
        }
    }

    twpLang.isRtlLanguage = function (langCode) {
        const rtl_langs = ['am', 'ar', 'fa', 'ha', 'he', 'ku', 'ps', 'ur', 'yi', 'iw']
        return rtl_langs.indexOf(langCode) !== -1
    }
